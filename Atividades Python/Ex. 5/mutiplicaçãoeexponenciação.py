# Solicita um número ao usuário
numero = float(input("Digite um número: "))

# Calcula o quadrado usando multiplicação
quadrado_mult = numero * numero
print(f"O quadrado de {numero} usando multiplicação é: {quadrado_mult}")

# Calcula o quadrado usando exponenciação
quadrado_exp = numero ** 2
print(f"O quadrado de {numero} usando exponenciação é: {quadrado_exp}")
