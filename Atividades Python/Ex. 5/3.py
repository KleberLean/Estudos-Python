# Solicita um número ao usuário
numero = float(input("Digite um número: "))

# Adiciona 10
numero += 10
print(f"Após adicionar 10: {numero}")

# Subtrai 5
numero -= 5
print(f"Após subtrair 5: {numero}")

# Multiplica por 2
numero *= 2
print(f"Após multiplicar por 2: {numero}")

# Divide por 3
numero /= 3
print(f"Após dividir por 3: {numero}")
